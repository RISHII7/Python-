# Print a poem about dawn
print("---A Glimpse of Dawn---")

# Define the poem as strings
poem = """
The morning light begins to rise,
A gentle warmth in sleepy skies.
The world awakens, soft and bright,
Embracing day, bidding night.

Whispers of wind through trees so tall,
Echo the dreams that night let fall.
Each step we take, a story told,
In every moment, young and old.
"""

# Print the poem
print(poem)
